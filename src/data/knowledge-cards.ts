// Helper functions
export const getCardsByCategory = (category: KnowledgeCategory): KnowledgeCard[] => 
  knowledgeCards.filter(card => card.category === category);

export const getCardsByDifficulty = (difficulty: DifficultyLevel): KnowledgeCard[] => 
  knowledgeCards.filter(card => card.difficulty === difficulty);

export const getCardById = (id: string): KnowledgeCard | undefined => 
  knowledgeCards.find(card => card.id === id);

export const searchCards = (query: string): KnowledgeCard[] => {
  const lowercaseQuery = query.toLowerCase();
  return knowledgeCards.filter(card => 
    card.title.toLowerCase().includes(lowercaseQuery) ||
    card.titleMm.includes(query) ||
    card.summary.toLowerCase().includes(lowercaseQuery) ||
    card.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
};

export const getRandomCards = (count: number): KnowledgeCard[] => {
  const shuffled = [...knowledgeCards].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

