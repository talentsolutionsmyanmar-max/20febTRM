// Get featured companies
export const getFeaturedCompanies = (): Company[] => {
  return companies.filter(c => c.featured);
};

// Get companies by industry
export const getCompaniesByIndustry = (industry: string): Company[] => {
  if (industry === 'All') return companies;
  return companies.filter(c => c.industry === industry);
};

// Get top rated companies (for People's Award)
export const getTopRatedCompanies = (limit: number = 20): Company[] => {
  return [...companies]
    .sort((a, b) => (b.voteCount || 0) - (a.voteCount || 0))
    .slice(0, limit);
};

// Get award winners
export const getAwardWinners = (year: number): Company[] => {
  return companies
    .filter(c => c.awardYear === year)
    .sort((a, b) => (a.awardRank || 0) - (b.awardRank || 0));
};

// Search companies
export const searchCompanies = (query: string): Company[] => {
  const lowerQuery = query.toLowerCase();
  return companies.filter(c => 
    c.name.toLowerCase().includes(lowerQuery) ||
    c.industry.toLowerCase().includes(lowerQuery) ||
    c.location.toLowerCase().includes(lowerQuery) ||
    c.nameMm?.includes(query)
  );
};
